//Bucle 'do while'. El código se repetirá una y otra vez siempre que se cumpla la condición.
//En este caso siempre se ejecutará primero el código antes de validar la condición. 
// do{
//     código a ejecutar
// }while(condición)

let contador = 0;

do{
    console.log(contador);
    contador++;
}while(contador < 10);